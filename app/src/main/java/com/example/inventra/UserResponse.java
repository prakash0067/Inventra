package com.example.inventra;

public class UserResponse {
    private int user_id;
    private String name;
    private String user_type;
    private String profile_pic;

    // Getters
    public int getUserId() {
        return user_id;
    }

    public String getName() {
        return name;
    }

    public String getUserType() {
        return user_type;
    }

    public String getProfilePic() {
        return profile_pic;
    }
}
